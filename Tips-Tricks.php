                                        <h1>
                                            What is Melody in Music?
                                        </h1>
                                    </div>
                                    <div class="c-i-main">
                                        <p>
                                            Melody is a succession of notes or voices arranged into a musical phrase. A songâ€™s melody is often the most memorable and recognizable part.<br><br>    
                                            Melodies can be created with instruments or vocals. They have two or more notes in a sequence that sound musically pleasing. Most compositions consist of multiple melodies that repeat. <br> <br>
                                            The two primary elements of a melody are <span>Pitch</span> and <span>Rhythm</span> :<br>
                                            <br>
                                            <ul class="c-i-ul">
                                                <li>
                                                    Pitch: <span>is the audio vibration produced by an instrument or voice. Itâ€™s how high or low a note will sound. Arranging these pitches in a series creates a melody.</span>
                                                </li>
                                                <li>
                                                    Rhythm: <span>or duration is the length each pitch will sound. These durations are divided into beat divisions like whole notes, half notes, quarter notes, triplets, etc.</span>
                                                </li>
                                            </ul>
                                            <br>
                                            Melodies also have two types of melodic motion: Conjunct  or Disjunct :<br>
                                            <br>
                                            <ul class="c-i-ul">
                                                <li>
                                                    Conjunct motion: <span> is when notes move by whole or half steps. Conjunct is also the most natural and comfortable to play and sing. There are shorter leaps between notes.</span>
                                                </li>
                                                <li>
                                                    Disjunct motion: <span>has larger leaps between notes. Large interval leaps between notes can make the melody difficult to play or sing. Disjunct motion is also less memorable and smooth sounding.</span>
                                                </li>
                                            </ul>
                                            <br>
                                            Musicians combine conjunct and disjunct motion to give melodies more variation and interest.<br>
                                        </p>
