                                        <h1>
                                            What is Harmony in Music?
                                        </h1>
                                    </div>
                                    <div class="c-i-main">
                                        <p>
                                            Harmony is when multiple notes or voices play simultaneously to produce a new sound. The combined sounds in harmonies complement one another and sound pleasing.<br><br>    
                                            Chords and chord progressions are examples of harmony. A chord has three or more notes that play at the same time. The chords and chord progressions in a piece of music support or complement the melody. <br> <br>
                                            Combining vocal parts also creates harmony. The combined voices of a choir are a perfect example. The multiple voices that make up a choir blend to make a harmonious sound. <br> <br>
                                            However, not all harmonies are pleasing to our ear. There are two main types of harmony: <span>Dissonant</span> and <span>Consonant</span>: <br>
                                            <br>
                                            <ul class="c-i-ul">
                                                <li>
                                                    Dissonant harmony: <span>adds notes that do not sound pleasant when played together. The result adds tension and makes the chord sound unstable. This tension releases by resolving to consonant chords. Dissonant interval examples are seconds, sevenths, and ninths.</span>
                                                </li>
                                                <li>
                                                    Consonant harmony: <span>sounds stable and pleasing. All the notes in a consonant chord have intervals that play nicely together. Constant chords also transition smoothly with one another in a progression. Consonant interval examples are unison, thirds, fifths, and octaves.</span>
                                                </li>
                                            </ul>
                                            <br>
                                            Musicians combine consonant and dissonant harmonies to make the music more exciting and intriguing.<br>
                                        </p>
                                    </div>
